<p>Each player receives 2 private <em>hole cards</em> (yours alone). Up to 5 <em>community cards</em> are placed face-up on the board — shared by all players. You make the best 5-card hand from any combination of your 2 hole cards and the 5 board cards.</p>

<p><strong>Betting rounds</strong> — in order:</p>

<ol>
  <li><strong>Pre-flop</strong> — hole cards are dealt; first betting round</li>
  <li><strong>Flop</strong> — first 3 board cards revealed; second betting round</li>
  <li><strong>Turn</strong> — 4th board card; third betting round</li>
  <li><strong>River</strong> — 5th board card; final betting round</li>
</ol>

<p><strong>Showdown:</strong> When all betting is done, remaining players reveal their hands. The highest-ranked 5-card hand wins — or the last player standing if everyone else folds.</p>

<p><strong>Actions available each round:</strong> Check (no bet owed, pass), Bet/Call (match the bet), Raise (increase the bet), Fold (surrender your hand).</p>

