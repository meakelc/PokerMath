<dl>
  <div class="term-group">
    <dt>Out</dt>
    <dd>An unseen card that, if dealt, would complete your hand into a likely winner. Example: with 4 hearts showing, any of the remaining 9 hearts is an out.</dd>
  </div>

  <div class="term-group">
    <dt>Equity</dt>
    <dd>Your hand's probability of winning at showdown, expressed as a percentage. Example: 9 outs × 4 (two streets) ≈ 36%.</dd>
  </div>

  <div class="term-group">
    <dt>Street</dt>
    <dd>A round of community-card dealing: Flop (3 cards), Turn (1 card), River (1 card). Two streets remain after the Flop.</dd>
  </div>

  <div class="term-group">
    <dt>Board</dt>
    <dd>The community cards placed face-up in the center for all players to use.</dd>
  </div>

  <div class="term-group">
    <dt>Pot odds</dt>
    <dd>The ratio of the total pot to the cost of a call. Example: $50 pot and $10 to call = 5:1 pot odds.</dd>
  </div>

  <div class="term-group">
    <dt>Required equity</dt>
    <dd>The break-even equity threshold from pot odds: cost ÷ (pot + cost). At 5:1 odds: 10 ÷ 60 ≈ 16.7%.</dd>
  </div>

  <div class="term-group">
    <dt>Expected value (EV)</dt>
    <dd>The average outcome of a decision repeated many times. Positive EV means profitable long-term, even if it loses any individual hand.</dd>
  </div>

  <div class="term-group">
    <dt>All-in</dt>
    <dd>Betting all remaining chips. No more action is owed from that player; the hand goes to showdown.</dd>
  </div>

  <div class="term-group term-group--last">
    <dt>Showdown</dt>
    <dd>The point where remaining players reveal their hands and the winner is determined.</dd>
  </div>
</dl>

